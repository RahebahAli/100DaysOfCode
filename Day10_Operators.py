print(x)
x &= 2 # And : in binary number
print(x)
x ^= 4 # Xor : in binary number
print(x)
x <<= 5 # Left shift  in binary number, same as : x * (2 ** 5)
print(x)
x >>= 6 # Right shift  in binary number, same as : x / (2 ** 5)
print(x)


